nome = "Sheila Driele Reis Santos"

print(nome[0]) #Pegando apenas uma posição da string
print(nome[-1]) #Pega a ultima letra
print(nome[8]) #uma posição
print(nome[:13]) #Determinando somente o fim
print(nome[7:]) #Determinando o começo
print(nome[14:18]) #Determinando o inicio e o fim
print(nome[7:18:2]) #Pega as posições pulando 2 passos
print(nome[:])
print(nome[::-1]) #faz o espelhamento da string